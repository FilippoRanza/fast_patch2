from file_utils import Inplace
from line_refactor import refactor_line, reset_refactor


def refactor_file(file_name, bak):
    with Inplace(file_name, bak) as f:
        for l in f:
            tmp = refactor_line(l)
            f.write(tmp)

    reset_refactor()


def main():
    refactor_file('test.tex', 'bak')


if __name__ == '__main__':
    main()




